--计算学生数量权重系数B
DELIMITER //
CREATE TRIGGER calculate_student_weight BEFORE INSERT ON `本科工作量课程排序表`
FOR EACH ROW
BEGIN
    DECLARE student_weight FLOAT(255, 2);
    IF NEW.选课人数 <= 45 THEN
        SET student_weight = 0;
    ELSEIF NEW.选课人数 > 45 AND NEW.选课人数 <= 55 THEN
        SET student_weight = 0.1;
    ELSEIF NEW.选课人数 > 55 AND NEW.选课人数 <= 65 THEN
        SET student_weight = 0.2;
    ELSE
        SET student_weight = 0.3;
    END IF;
    SET NEW.学生数量权重系数B = student_weight;
END//
DELIMITER ;



--计算理论课总学时，实验课总学时，本科课程总学时
DELIMITER //
CREATE TRIGGER calculate_hours BEFORE INSERT ON `本科工作量课程排序表`
FOR EACH ROW
BEGIN
    SET NEW.理论课总学时P1 = (NEW.研讨学时 + NEW.授课学时) * NEW.课程类型系数A * (1 + NEW.学生数量权重系数B);
    SET NEW.实验课总学时P2 = NEW.实验学时 * NEW.实验分组数 * NEW.实验课系数;
    SET NEW.本科课程总学时 = NEW.理论课总学时P1 + NEW.实验课总学时P2;
END//
DELIMITER ;



