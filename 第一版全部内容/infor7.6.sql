CREATE TRIGGER update_teacher_workload AFTER INSERT ON `本科工作量课程排序表`
FOR EACH ROW
BEGIN
    DECLARE total_course_hours float(255, 2);
    DECLARE total_undergraduate_hours float(255, 2);
    DECLARE graduate_count int(0);
    DECLARE teaching_internship_count int(0);
    DECLARE undergraduate_competition_p float(255, 2);
    DECLARE undergraduate_mentorship_count int(0);
    DECLARE research_project_p float(255, 2);
    DECLARE excellent_course_count int(0);
    DECLARE teaching_award_count int(0);
    DECLARE announcement_service_hours float(255, 2);
    
    -- 计算本科课程总学时和课程总学时
    SELECT SUM(`本科课程总学时`) INTO total_undergraduate_hours FROM `本科工作量课程排序表` WHERE `教工号` = NEW.`教工号`;
    SELECT SUM(`课程总学时`) INTO total_course_hours FROM `本科工作量课程排序表` WHERE `教工号` = NEW.`教工号`;

    
    -- 更新本科工作量教师排序表
    UPDATE `本科工作量教师排序表` SET 
        `教师名称` = NEW.`教师名称`,
        `本科课程总学时` = total_undergraduate_hours,
        `课程总学时` = total_course_hours

    WHERE `教工号` = NEW.`教工号`;
END//
