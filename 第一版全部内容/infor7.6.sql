DELIMITER //
CREATE PROCEDURE recompute_workload()
BEGIN
    -- 清空本科工作量教师排序表
    TRUNCATE TABLE `本科工作量教师排序表`;
    
    -- 插入符合要求的记录到本科工作量教师排序表
    INSERT INTO `本科工作量教师排序表` (教工号)
    SELECT DISTINCT 教工号 FROM `本科工作量课程排序表`;
    
UPDATE `本科工作量教师排序表` AS t1
INNER JOIN `本科工作量课程排序表` AS t2 ON t1.`教工号` = t2.`教工号`
SET t1.`教师名称` = t2.`教师名称`;

    -- 更新本科工作量教师排序表中的记录
UPDATE `本科工作量教师排序表` AS t1
INNER JOIN (
    SELECT 
        教工号,
        SUM(`本科课程总学时`) AS 本科课程总学时,
        SUM(`课程总学时`) AS 课程总学时
    FROM `本科工作量课程排序表` AS t2
    GROUP BY 教工号
) AS t3 ON t1.`教工号` = t3.`教工号`
SET 
    t1.`本科课程总学时` = t3.`本科课程总学时`,
    t1.`课程总学时` = t3.`课程总学时`;

UPDATE `本科工作量教师排序表` AS t1
INNER JOIN (
    SELECT 
        `毕业论文指导老师`,
        COUNT(*) AS 毕业论文学生人数
    FROM `毕业论文`
    GROUP BY `毕业论文指导老师`
) AS t2 ON t1.`教师名称` = t2.`毕业论文指导老师`
SET t1.`毕业论文学生人数` = t2.`毕业论文学生人数`;

UPDATE `本科工作量教师排序表`
SET `毕业论文P` = `毕业论文学生人数` * 8;

UPDATE `本科工作量教师排序表` AS t1
SET t1.`指导教学实习人数` = COALESCE((
    SELECT COUNT(*)
    FROM `本科实习` AS t2
    WHERE t2.`学部内实习指导教师` = t1.`教师名称`
), 0);

UPDATE `本科工作量教师排序表`
SET `指导教学实习周数` = `指导教学实习人数` * 6;

UPDATE `本科工作量教师排序表`
SET `指导教学实习P` = (`指导教学实习周数` * `指导教学实习人数` * 6) / 8;

UPDATE `本科工作量教师排序表`
SET `负责实习点建设与管理P` = `指导教学实习P`;


UPDATE `本科工作量教师排序表` AS t1
SET t1.`指导本科生竞赛P` = COALESCE((
    SELECT COUNT(*)
    FROM `学生竞赛` AS t2
    WHERE FIND_IN_SET(t1.`教师名称`, REPLACE(t2.`指导教师`, '、', ',')) > 0
), 0);

UPDATE `本科工作量教师排序表` AS t1
SET t1.`指导本科生科研P` = COALESCE((
    SELECT COUNT(*)
    FROM `学生科研` AS t2
    WHERE t1.`教师名称` = t2.`指导老师`
), 0);

UPDATE `本科工作量教师排序表` AS t1
SET t1.`本科生导师制` = COALESCE((
    SELECT COUNT(*)
    FROM `本科生导师制` AS t2
    WHERE t1.`教师名称` = t2.`导师姓名`
), 0);

UPDATE `本科工作量教师排序表` AS t1
LEFT JOIN `教研项目2` AS t2 ON t1.`教师名称` = t2.`教师姓名`
SET t1.`教研教改P` = COALESCE(t2.`教研项目工作量`, 0);



UPDATE `本科工作量教师排序表` AS t1
LEFT JOIN `一流课程2` AS t2 ON t1.`教师名称` = t2.`教师姓名`
SET t1.`一流课程` = COALESCE(t2.`工作量`, 0);

UPDATE `本科工作量教师排序表` AS t1
LEFT JOIN (
  SELECT `教师`, SUM(`教学成果工作量`) AS `教学成果奖工作量`
  FROM `教学成果奖2`
  GROUP BY `教师`
) AS t2 ON t1.`教师名称` = t2.`教师`
SET t1.`教学成果奖` = COALESCE(t2.`教学成果奖工作量`, 0);

UPDATE `本科工作量教师排序表` AS t1
SET t1.`公共服务` = COALESCE((
    SELECT SUM(`课时`)
    FROM `公共服务` AS t2
    WHERE t2.`姓名` = t1.`教师名称`
), 0);



END//
DELIMITER ;